# v1.4.5: end-to-end fresh-install flow via auth_bootstrap.
# The shipped .mcp.json now has TD_MCP_AUTOGENERATE_SECRET=1, so a fresh
# plugin install with no ~/.tdpilot-dpsk4/.tdpilot-dpsk4.env file must:
#   1. boot up,
#   2. have bootstrap_auth mint a secret into an ephemeral env file,
#   3. pass verify_auth_config.
# ---------------------------------------------------------------------------


def test_fresh_plugin_install_end_to_end_bootstraps_and_passes(monkeypatch, tmp_path):
    """Reproduces the fresh-plugin-install flow that v1.4.5 fixes.

    Pre-v1.4.5: shipped .mcp.json had REQUIRE_AUTH=1 + no autogen flag,
    so bootstrap had nothing to do and verify_auth_config tripped.
    Post-v1.4.5: shipped .mcp.json adds AUTOGENERATE_SECRET=1, so
    bootstrap mints a secret and verify passes.
    """
    from td_mcp import auth_bootstrap

    env = _shipped_mcp_env()
    _apply_env(monkeypatch, env, secret=None)
    env_file = tmp_path / ".tdpilot-dpsk4.env"

    auth_bootstrap.bootstrap_auth(env_file)

    # Secret was minted into both env and file
    assert os.environ.get("TD_MCP_SHARED_SECRET")
    assert env_file.exists()
    # The gate now passes
    server.verify_auth_config()


def test_doctor_also_runs_bootstrap_before_auth_check(monkeypatch, tmp_path):
    """v1.4.5 live-test regression: `tdpilot-dpsk4 doctor` must call
    bootstrap_auth() before collecting the report, otherwise the
    auth_config line lies about needing install.sh when autogen was
    supposed to handle it.

    Caught by running (before the fix):
      TDPILOT_ENV_FILE=<tmp> TD_MCP_AUTOGENERATE_SECRET=1 \\
      TD_MCP_REQUIRE_AUTH=1 uv run tdpilot-dpsk4 doctor --skip-td-check
    → auth_config FAIL even though autogen should have minted a secret.

    The bootstrap code path was only wired into the `run` command, not
    `doctor`. v1.4.5 moves bootstrap to the top of main() for all
    non-init commands.
    """
    env_file = tmp_path / ".tdpilot-dpsk4.env"
    monkeypatch.setenv("TD_MCP_REQUIRE_AUTH", "1")
    monkeypatch.setenv("TD_MCP_AUTOGENERATE_SECRET", "1")
    monkeypatch.delenv("TD_MCP_SHARED_SECRET", raising=False)
    monkeypatch.setenv("TDPILOT_ENV_FILE", str(env_file))

    with pytest.raises(SystemExit) as exc:
        server.main(["doctor", "--skip-td-check"])

    # Bootstrap ran → secret minted → auth_config PASS → doctor exits 0
    assert exc.value.code == 0, (
        "doctor exited non-zero; bootstrap_auth probably isn't wired into the doctor command path"
    )
    assert env_file.exists(), "doctor did not trigger bootstrap_auth — env file never created"


def test_fresh_plugin_install_with_autogen_disabled_still_trips_gate(monkeypatch, tmp_path):
    """Negative control: if someone removes TD_MCP_AUTOGENERATE_SECRET=1
    from .mcp.json, the fail-loud behavior returns. This is by design —
    bootstrap_auth is opt-in, not silent."""
    from td_mcp import auth_bootstrap

    env = _shipped_mcp_env()
    # Simulate the mis-configuration: autogen removed
    env.pop("TD_MCP_AUTOGENERATE_SECRET", None)
    _apply_env(monkeypatch, env, secret=None)
    monkeypatch.delenv("TD_MCP_AUTOGENERATE_SECRET", raising=False)
    env_file = tmp_path / ".tdpilot-dpsk4.env"

    auth_bootstrap.bootstrap_auth(env_file)

    # No secret should have been generated
    assert "TD_MCP_SHARED_SECRET" not in os.environ
    assert not env_file.exists()
    # Gate trips
    with pytest.raises(RuntimeError):
        server.verify_auth_config()
